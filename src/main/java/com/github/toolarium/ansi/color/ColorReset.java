package com.github.toolarium.ansi.color;


/**
 * Reset options.
 * 
 * @author patrick
 */
public enum ColorReset {
    /**
     * Resets foreground color
     */
    FG,
    
    /**
     * Resets background color
     */
    BG,
    
    /**
     * Resets foreground and background color.
     */
    ALL
}
